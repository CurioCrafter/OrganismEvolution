glm::vec3 MarchingCubes::interpolateVertex(
    const glm::vec3& p1, const glm::vec3& p2,
    float val1, float val2, float isovalue
) {
    if (std::abs(isovalue - val1) < 0.00001f) return p1;
    if (std::abs(isovalue - val2) < 0.00001f) return p2;
    if (std::abs(val1 - val2) < 0.00001f) return p1;

    float mu = (isovalue - val1) / (val2 - val1);
    return p1 + mu * (p2 - p1);
}

MeshData MarchingCubes::generateMesh(
    const MetaballSystem& metaballs,
    int resolution,
    float isovalue
) {
    MeshData mesh;

    if (metaballs.getMetaballCount() == 0) {
        return mesh;
    }

    // Get bounds
    glm::vec3 minBounds, maxBounds;
    metaballs.getBounds(minBounds, maxBounds);

    // Add padding
    minBounds -= glm::vec3(1.0f);
    maxBounds += glm::vec3(1.0f);

    glm::vec3 size = maxBounds - minBounds;
    glm::vec3 step = size / float(resolution);

    std::cout << "Generating mesh: resolution=" << resolution
              << ", bounds=(" << minBounds.x << "," << minBounds.y << "," << minBounds.z
              << ") to (" << maxBounds.x << "," << maxBounds.y << "," << maxBounds.z << ")"
              << std::endl;

    // Sample grid
    std::vector<float> gridValues;
    gridValues.resize((resolution + 1) * (resolution + 1) * (resolution + 1));

    // Evaluate potential at each grid point
    float minPotential = 999.0f;
    float maxPotential = -999.0f;
    int pointsAboveIso = 0;

    for (int z = 0; z <= resolution; z++) {
        for (int y = 0; y <= resolution; y++) {
            for (int x = 0; x <= resolution; x++) {
                glm::vec3 pos = minBounds + glm::vec3(x, y, z) * step;
                int index = x + y * (resolution + 1) + z * (resolution + 1) * (resolution + 1);
                float potential = metaballs.evaluatePotential(pos);
                gridValues[index] = potential;

                minPotential = std::min(minPotential, potential);
                maxPotential = std::max(maxPotential, potential);
                if (potential > isovalue) pointsAboveIso++;
            }
        }
    }

    // Debug output for potential field analysis
    std::cout << "  Potential range: [" << minPotential << ", " << maxPotential << "]"
              << ", points above isovalue(" << isovalue << "): " << pointsAboveIso << std::endl;

    // March through cubes
    std::unordered_map<uint64_t, unsigned int> vertexCache;

    auto getGridValue = [&](int x, int y, int z) -> float {
        return gridValues[x + y * (resolution + 1) + z * (resolution + 1) * (resolution + 1)];
    };

    auto getGridPos = [&](int x, int y, int z) -> glm::vec3 {
        return minBounds + glm::vec3(x, y, z) * step;
    };

    for (int z = 0; z < resolution; z++) {
        for (int y = 0; y < resolution; y++) {
            for (int x = 0; x < resolution; x++) {
                // Get cube corner values
                float cubeValues[8];
                glm::vec3 cubePositions[8];

                cubePositions[0] = getGridPos(x, y, z);
                cubePositions[1] = getGridPos(x + 1, y, z);
                cubePositions[2] = getGridPos(x + 1, y, z + 1);
                cubePositions[3] = getGridPos(x, y, z + 1);
                cubePositions[4] = getGridPos(x, y + 1, z);
                cubePositions[5] = getGridPos(x + 1, y + 1, z);
                cubePositions[6] = getGridPos(x + 1, y + 1, z + 1);
                cubePositions[7] = getGridPos(x, y + 1, z + 1);

                cubeValues[0] = getGridValue(x, y, z);
                cubeValues[1] = getGridValue(x + 1, y, z);
                cubeValues[2] = getGridValue(x + 1, y, z + 1);
                cubeValues[3] = getGridValue(x, y, z + 1);
                cubeValues[4] = getGridValue(x, y + 1, z);
                cubeValues[5] = getGridValue(x + 1, y + 1, z);
                cubeValues[6] = getGridValue(x + 1, y + 1, z + 1);
                cubeValues[7] = getGridValue(x, y + 1, z + 1);

                // Determine cube configuration
                // Use > because metaball potential is HIGH inside (near 1.0) and LOW outside (near 0.0)
                // Vertices with potential > isovalue are "inside" the surface
                int cubeIndex = 0;
                if (cubeValues[0] > isovalue) cubeIndex |= 1;
                if (cubeValues[1] > isovalue) cubeIndex |= 2;
                if (cubeValues[2] > isovalue) cubeIndex |= 4;
                if (cubeValues[3] > isovalue) cubeIndex |= 8;
                if (cubeValues[4] > isovalue) cubeIndex |= 16;
                if (cubeValues[5] > isovalue) cubeIndex |= 32;
                if (cubeValues[6] > isovalue) cubeIndex |= 64;
                if (cubeValues[7] > isovalue) cubeIndex |= 128;

                // Skip if entirely inside or outside
                if (edgeTable[cubeIndex] == 0) continue;

                // Calculate edge vertices
                glm::vec3 edgeVertices[12];
                int edgeFlags = edgeTable[cubeIndex];

                if (edgeFlags & 1) edgeVertices[0] = interpolateVertex(cubePositions[0], cubePositions[1], cubeValues[0], cubeValues[1], isovalue);
                if (edgeFlags & 2) edgeVertices[1] = interpolateVertex(cubePositions[1], cubePositions[2], cubeValues[1], cubeValues[2], isovalue);
                if (edgeFlags & 4) edgeVertices[2] = interpolateVertex(cubePositions[2], cubePositions[3], cubeValues[2], cubeValues[3], isovalue);
                if (edgeFlags & 8) edgeVertices[3] = interpolateVertex(cubePositions[3], cubePositions[0], cubeValues[3], cubeValues[0], isovalue);
                if (edgeFlags & 16) edgeVertices[4] = interpolateVertex(cubePositions[4], cubePositions[5], cubeValues[4], cubeValues[5], isovalue);
                if (edgeFlags & 32) edgeVertices[5] = interpolateVertex(cubePositions[5], cubePositions[6], cubeValues[5], cubeValues[6], isovalue);
                if (edgeFlags & 64) edgeVertices[6] = interpolateVertex(cubePositions[6], cubePositions[7], cubeValues[6], cubeValues[7], isovalue);
                if (edgeFlags & 128) edgeVertices[7] = interpolateVertex(cubePositions[7], cubePositions[4], cubeValues[7], cubeValues[4], isovalue);
                if (edgeFlags & 256) edgeVertices[8] = interpolateVertex(cubePositions[0], cubePositions[4], cubeValues[0], cubeValues[4], isovalue);
                if (edgeFlags & 512) edgeVertices[9] = interpolateVertex(cubePositions[1], cubePositions[5], cubeValues[1], cubeValues[5], isovalue);
                if (edgeFlags & 1024) edgeVertices[10] = interpolateVertex(cubePositions[2], cubePositions[6], cubeValues[2], cubeValues[6], isovalue);
                if (edgeFlags & 2048) edgeVertices[11] = interpolateVertex(cubePositions[3], cubePositions[7], cubeValues[3], cubeValues[7], isovalue);

                // Generate triangles
                for (int i = 0; triTable[cubeIndex][i] != -1; i += 3) {
                    for (int j = 0; j < 3; j++) {
                        int edgeIndex = triTable[cubeIndex][i + j];
                        glm::vec3 pos = edgeVertices[edgeIndex];
                        glm::vec3 normal = metaballs.calculateNormal(pos);

                        mesh.vertices.push_back(Vertex(pos, normal));
                        mesh.indices.push_back(static_cast<unsigned int>(mesh.vertices.size() - 1));
                    }
                }
            }
        }
    }

    std::cout << "Generated " << mesh.vertices.size() << " vertices, "
              << mesh.indices.size() / 3 << " triangles" << std::endl;

    // Add warning if mesh is empty
    if (mesh.vertices.empty()) {
        std::cerr << "[MARCHING CUBES ERROR] Generated 0 vertices! "
                  << "Isovalue " << isovalue << " may not intersect the metaball field. "
                  << "Try a different isovalue or use generateFallbackSphere()." << std::endl;
    }

    mesh.calculateBounds();
    return mesh;
}

MeshData MarchingCubes::generateFallbackSphere(float radius, int segments, int rings) {
    MeshData mesh;

    std::cout << "[FALLBACK] Generating sphere mesh: radius=" << radius
              << ", segments=" << segments << ", rings=" << rings << std::endl;

    const float PI = 3.14159265358979f;

    // Generate vertices for a UV sphere
    for (int ring = 0; ring <= rings; ring++) {
        float phi = PI * static_cast<float>(ring) / static_cast<float>(rings);  // 0 to PI
        float y = std::cos(phi) * radius;
        float ringRadius = std::sin(phi) * radius;

        for (int seg = 0; seg <= segments; seg++) {
            float theta = 2.0f * PI * static_cast<float>(seg) / static_cast<float>(segments);  // 0 to 2*PI
            float x = std::cos(theta) * ringRadius;
            float z = std::sin(theta) * ringRadius;

            glm::vec3 position(x, y, z);
            glm::vec3 normal = glm::normalize(position);  // For a sphere centered at origin, normal = position normalized
            glm::vec2 texCoord(
                static_cast<float>(seg) / static_cast<float>(segments),
                static_cast<float>(ring) / static_cast<float>(rings)
            );

            mesh.vertices.emplace_back(position, normal, texCoord);
        }
    }

    // Generate indices (two triangles per quad)
    for (int ring = 0; ring < rings; ring++) {
        for (int seg = 0; seg < segments; seg++) {
            unsigned int curr = ring * (segments + 1) + seg;
            unsigned int next = curr + segments + 1;

            // First triangle (counter-clockwise winding)
            mesh.indices.push_back(curr);
            mesh.indices.push_back(next);
            mesh.indices.push_back(curr + 1);

            // Second triangle (counter-clockwise winding)
            mesh.indices.push_back(curr + 1);
            mesh.indices.push_back(next);
            mesh.indices.push_back(next + 1);
        }
    }

    std::cout << "[FALLBACK] Sphere generated: " << mesh.vertices.size()
              << " vertices, " << mesh.indices.size() << " indices ("
              << mesh.indices.size() / 3 << " triangles)" << std::endl;

    mesh.calculateBounds();
    return mesh;
}
